function formulario() {
  var nome = document.createElement("input");
  var email = document.createElement("input");
  var botao = document.createElement("input");

  nome.setAttribute("placeholder", "nome");
  email.setAttribute("type", "email");
  email.setAttribute("placeholder", "email");
  botao.nome.setAttribute("type", "submit");
  botao.nome.setAttribute("value", "enviar dados");

  document.getElementById("nome").append;
  document.getElementById("email").append;
  document.getElementById("botao").append;
}
